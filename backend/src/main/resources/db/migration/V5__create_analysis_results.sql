-- V5__create_analysis_results.sql
-- [목적] LLM 분석 결과(Stage 2~5) — 신뢰도 confidence 필수, 공시당 최종 1건 (db_schema.md 3.5).
-- [이유] 호재/중립/악재 분류·요약·근거의 저장처. 환각 방지 위해 스키마 파싱 후 저장(CLAUDE.md 6-6).
-- [사이드 임팩트] V9 feedbacks가 analysis_results(id) FK 참조. 알림 발송 시 노출 범위 판단 근거.
-- [수정 시 고려사항] confidence 없는 호재/악재 단정 금지. is_withheld=TRUE면 "판단 보류" 표시.
--                  stage_details(JSONB)는 원응답 파싱 후 저장. disclosures 의존(V4 이후).

CREATE TABLE analysis_results (
    id                  BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    disclosure_id       BIGINT       NOT NULL REFERENCES disclosures (id) ON DELETE CASCADE,
    sentiment           VARCHAR(10)  NOT NULL
                                     CHECK (sentiment IN ('POSITIVE','NEUTRAL','NEGATIVE')),
    confidence          NUMERIC(4,3) NOT NULL CHECK (confidence >= 0 AND confidence <= 1),
    is_withheld         BOOLEAN      NOT NULL DEFAULT FALSE,   -- 신뢰도 낮음 → 판단 보류
    summary             TEXT         NOT NULL,                 -- 자연어 요약(3줄)
    expected_reaction   VARCHAR(20)  CHECK (expected_reaction IN ('UP','FLAT','DOWN')),  -- Stage 4
    rationale           TEXT,                                  -- 근거(과거 사례 종합)
    stage_reached       SMALLINT     NOT NULL CHECK (stage_reached BETWEEN 1 AND 5),
    stage_details       JSONB,                                 -- Stage별 중간 산출(스키마 파싱 후 저장)
    model_name          VARCHAR(50),                           -- 사용 LLM(미정, 런타임 기록)
    input_tokens        INTEGER,
    output_tokens       INTEGER,
    created_at          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT uq_analysis_disclosure UNIQUE (disclosure_id)   -- 공시당 최종 분석 1건
);

COMMENT ON COLUMN analysis_results.confidence    IS 'LLM 신뢰도 0~1. 낮으면 is_withheld=TRUE(판단 보류)';
COMMENT ON COLUMN analysis_results.stage_details IS 'Stage 2~5 원응답 — 항상 스키마(record/Zod) 파싱 후 저장(환각 방지)';

CREATE INDEX idx_analysis_disclosure ON analysis_results (disclosure_id);
CREATE INDEX idx_analysis_sentiment  ON analysis_results (sentiment);
